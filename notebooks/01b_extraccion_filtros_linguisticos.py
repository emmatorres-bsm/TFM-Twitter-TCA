def fetch_all_tweets(query: str, api_key: str):
    base_url = "https://api.twitterapi.io/twitter/tweet/advanced_search"
    headers = {"x-api-key": api_key}
    all_tweets = []
    seen_tweet_ids = set()
    cursor = None
    last_min_id = None
    max_retries = 3
    page_count = 0
    max_pages = 60

    while True:
        params = {"query": query}
        if cursor: params["cursor"] = cursor
        elif last_min_id: params["query"] = f"{query} max_id:{last_min_id}"

        retry_count = 0
        while retry_count < max_retries:
            try:
                response = requests.get(base_url, headers=headers, params=params)
                response.raise_for_status()
                data = response.json()
                tweets = data.get("tweets", [])
                has_next_page = data.get("has_next_page", False)
                cursor = data.get("next_cursor", None)

                new_tweets = [t for t in tweets if t.get("id") not in seen_tweet_ids]
                for tweet in new_tweets:
                    seen_tweet_ids.add(tweet.get("id"))
                    all_tweets.append(tweet)

                page_count += 1
                if page_count >= max_pages or not tweets:
                    return all_tweets

                if new_tweets: last_min_id = new_tweets[-1].get("id")
                if not has_next_page: return all_tweets
                break

            except Exception as e:
                retry_count += 1
                if retry_count == max_retries: return all_tweets
                time.sleep(2 ** retry_count)
    return all_tweets

def parse_tweets(tweets, keywords_str):
    parsed = []
    for tweet in tweets:
        author = tweet.get("author", {})
        parsed.append({
            "text": tweet.get("text"),
            "createdAt": tweet.get("createdAt"),
            "id": tweet.get("id"),
            "userName": author.get("userName"),
            "location": author.get("location"),
            "likes": tweet.get("likeCount"),
            "lang": tweet.get("lang"),
            "keywords": keywords_str
        })
    return parsed

def generate_date_ranges(start_year, end_year):
    ranges = []
    for year in range(start_year, end_year + 1):
        for month in range(1, 13):
            days = calendar.monthrange(year, month)[1]
            ranges.append((f"{year}-{month:02d}-01", f"{year}-{month:02d}-{days:02d}"))
    return ranges

